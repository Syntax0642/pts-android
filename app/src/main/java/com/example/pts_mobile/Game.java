package com.example.pts_mobile;

public class Game {



    String namaGame;
    int gambargame;
    String descGame;
    String ratingGame;

    public String getNamaGame() {
        return namaGame;
    }

    public void setNamaGame(String namaGame) {
        this.namaGame = namaGame;
    }

    public int getGambargame() {
        return gambargame;
    }

    public void setGambargame(int gambargame) {
        this.gambargame = gambargame;
    }

    public String getDescGame() {
        return descGame;
    }

    public void setDescGame(String descGame) {
        this.descGame = descGame;
    }

    public String getRatingGame() {
        return ratingGame;
    }

    public void setRatingGame(String ratingGame) {
        this.ratingGame = ratingGame;
    }
}
